//20.What is the volume of a brick of ice-cream with length 25 cm,breadth 10 cm and height 8 cm?

class Program20{
    public static void main(String args[]){

      int length_of_brick = 25;
      int breadth_of_brick = 10;
      int height_of_brick = 8;

      float volume_of_brick = length_of_brick * breadth_of_brick * height_of_brick; 

      System.out.println("Volume of a brick of ice-cream : "+volume_of_brick+"cm\u00B3");
    }
}