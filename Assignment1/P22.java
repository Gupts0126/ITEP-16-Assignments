//22. A pond is 50 m long, 30 m wide and 2 m deep. Find the capacity of the pond in cubic metre.

class Program22{
    public static void main(String args[]){

      int length_of_pond = 50;
      int breadth_of_pond = 30;
      int height_of_pond = 2;

      float volume_of_pond = length_of_pond * breadth_of_pond * height_of_pond; 

      System.out.println("The capacity of the pond is : "+volume_of_pond+"m\u00B3");
    }
}