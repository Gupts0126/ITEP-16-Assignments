//40. Calculate the volume of the cylinder: r = 5 cm, h = 9 cm

class Program40 {

    public static void main(String[] args) {

        int radius = 5;
        int height = 9;

        double volume = Math.PI * radius*radius* height;

        System.out.println("Volume of the cylinder : " + volume + "cm\u00B3");
    }
}
